// Variables

/*

Good practice to use camel case for variable naming convention
  first letter of first word = lower case
  first letter of other words = upper case

  Ex: myVariable

End variables with semi-colon (;)

Variable types:
  Boolean (T/F or 0/1)
  String
  Integer
  Long
  Array
  Object

Note - with JavaScript you can change the variable type unlike some other languages in which you cannot

JavaScript is weakly typed because you do not need to declare variable type when declaring variables

  Weakly typed: var Age = 32;
  Strongly typed: int Age = 32;

*/
